/*
 * Sum of 12 for different data types--demonstrates concatenation 
 */

public class Twelve{
  public static void main (String [] args){
    System.out.println(12 + 12);
    System.out.println(12.0 + 12.0);
    System.out.println(12 + 12.0);
    System.out.println(12 + "12");
    System.out.println("12.0" + 12.0);
    System.out.println("12.0" + 12);
    System.out.println("12" + "12");
  }
}
